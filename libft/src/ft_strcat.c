char	*ft_strcat(char *s1, char const *s2)
{
	char	*st1;
	int		i;

	i = 0;
	st1 = s1;
	while (*st1)
		st1++;
	while (s2[i])
	{
		*st1 = s2[i];
		st1++;
		i++;
	}
	*st1 = '\0';
	return (s1);
}
