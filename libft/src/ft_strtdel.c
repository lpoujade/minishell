#include "libft.h"

int		ft_strtdel(char ***str)
{
	int	c;

	c = 0;
	while ((*str)[c])
	{
		free((*str)[c]);
		(*str)[c++] = NULL;
	}
	free(*str);
	*str = NULL;
	return (0);
}
